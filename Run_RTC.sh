#!/usr/bin/env bash
# =============================================================================
#  OPERA RTC-S1 时序预处理流水线  — 统一配置启动脚本
#  用法：bash run_RTC.sh
#  兼容：Windows Git Bash / WSL / Linux / macOS
# =============================================================================

# ─────────────────────────────────────────────────────────────────────────────
# [0] Python 解释器 & 脚本路径（通常只需改这一行）
# ─────────────────────────────────────────────────────────────────────────────
PYTHON="D:/anaconda/envs/RTC/python.exe"

# 脚本路径：与本 .sh 文件同目录，无需修改
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PY_SCRIPT="${SCRIPT_DIR}/RTC_v15.py"

# =============================================================================
# [1] 研究区域（AOI）
#     格式：WKT POLYGON，经度在前，纬度在后
#     可在 https://geojson.io 绘制多边形后转换为 WKT
# =============================================================================
AOI_WKT="POLYGON((122.098 31.4264,108.6305 31.1995,108.418 23.3491,118.5917 24.0058,122.4699 30.7441,122.098 31.4264))"

# ROI 矩形裁切范围：minLon minLat maxLon maxLat
# 用于 vsicurl 读取时裁窗口，比 AOI 略大即可
ROI_MINLON=118.0
ROI_MINLAT=27.2
ROI_MAXLON=123.0
ROI_MAXLAT=31.3

# =============================================================================
# [2] 时间范围
# =============================================================================
START_DATE="2016-01-01"
END_DATE="2025-12-31"

# =============================================================================
# [3] 极化方式
#     可选：VH（交叉极化，植被/水体敏感）
#           VV（同极化，地表结构敏感）
#           HV / HH（L波段雷达常用，S1不支持）
# =============================================================================
POLARIZATION="VH"

# =============================================================================
# [4] 输出根目录
#     程序自动创建：
#       {BASE_DIR}/downloads/YYYY-MM-DD_VH.tif   ← 单日 warp 结果（中间文件）
#       {BASE_DIR}/downloads/composite/           ← 合成+TV 最终结果
#     Windows 路径示例：E:/RTC/sentinel_1/VH
# =============================================================================
BASE_DIR="E:/RTC/sentinel_1/VH_2017"

# =============================================================================
# [5] 输出分辨率（度/像素）
#     0.00027 ≈ 30m（OPERA 原生分辨率，推荐）
#     0.00009 ≈ 10m（超采样，内存需求极大，慎用）
# =============================================================================
OUTPUT_RESOLUTION="0.00027"

# =============================================================================
# [6] 输出坐标系
#     EPSG:4326  = WGS84 地理坐标系（默认，度为单位）
#     EPSG:32650 = WGS84 / UTM Zone 50N（米为单位，华东地区推荐）
#     EPSG:32649 = WGS84 / UTM Zone 49N
#     注意：若改为 UTM，ROI_BOUNDS 应同样使用投影坐标（米）
# =============================================================================
OUTPUT_CRS="EPSG:4326"

# =============================================================================
# [7] 时间合成模式
#     10d     = 10 天滑动合成（推荐，2017 年双星重访约 5 天）
#     12d     = 12 天合成（精确匹配 S1 轨道周期）
#     monthly = 月度合成（覆盖率最高，时序分辨率低）
# =============================================================================
COMPOSITE_MODE="10d"

# =============================================================================
# [8] TV（全变分）去斑参数
#     TV_REG    : 正则化强度，越大越平滑（推荐范围 3~8）
#     TV_MAX    : 有效像素线性功率上限（推荐 1.0）
#     TV_MIN    : 有效像素线性功率下限（推荐 1e-7）
#     TV_INTERP : TV 前的空洞插值方法（bilinear / none）
#     TV_EPS    : TV 迭代收敛阈值（推荐 0.01）
# =============================================================================
TV_REG="5"
TV_MAX="1.0"
TV_MIN="1e-7"
TV_INTERP="bilinear"
TV_EPS="0.01"

# =============================================================================
# [9] 功能开关（true / false）
#     DO_TV      : 对合成结果做 TV 去斑（关闭可大幅提速，但噪声更强）
#     DO_CLIP    : 将输出裁切到 AOI 多边形范围内（关闭保留完整矩形）
# =============================================================================
DO_TV="true"
DO_CLIP="true"

# =============================================================================
# [10] 中间文件保留策略
#
#  ┌─────────────────────────────────────────────────────────────────────┐
#  │  文件层级说明                                                        │
#  │  ① 单日期 warp TIF  downloads/YYYY-MM-DD_VH.tif                     │
#  │     每天一个，warp 到统一网格后拼接，是合成的输入                     │
#  │  ② 原始合成图       composite/*_raw.tif（仅 DEBUG_RAW=true 时生成）  │
#  │     TV 去斑前的中间合成结果，仅调试用                                 │
#  │  ③ 最终结果         composite/*_TV_clip.tif  ← 始终保留              │
#  └─────────────────────────────────────────────────────────────────────┘
#
#  KEEP_DAILY           : 是否保留单日期 warp TIF（①）
#    true  → 保留（默认，可重复利用已有结果跳过重算）
#    false → 合成完成后自动删除，节省 ~10~20 GB 磁盘
#
#  DEBUG_RAW            : 是否生成原始合成图（②）
#    true  → 生成（调试时用于对比 TV 去斑前后效果）
#    false → 不生成（默认，节省磁盘）
#
#  KEEP_RAW_COMPOSITE   : 是否保留原始合成图（②，仅 DEBUG_RAW=true 时有效）
#    true  → 保留
#    false → TV 去斑完成后自动删除（默认）
# =============================================================================
KEEP_DAILY="true"
DEBUG_RAW="false"
KEEP_RAW_COMPOSITE="false"

# =============================================================================
# [11] 并发与重试参数
#      WORKERS     : 日期并发线程数（建议 2，过高易触发 DNS 限速）
#      MAX_RETRIES : 网络错误最大重试次数，退避间隔 3/6/12/24/48s
#      RETRY_DELAY : 首次重试等待秒数（指数底数）
# =============================================================================
WORKERS="3"
MAX_RETRIES="5"
RETRY_DELAY="3"

# =============================================================================
# [12] Earthdata 认证
#
#  程序认证优先级：
#    1. Cookie 文件存在且有效（20h 内）→ 直接复用，无需联网
#    2. Cookie 不存在或已过期        → 通过 ~/.netrc 重新登录
#
#  COOKIE_FILE : Cookie 文件路径（可选，留空或指向不存在的文件均可）
#    有 Cookie  → 设置绝对路径，例如 "D:/sentinel_1/cookies.txt"
#    仅用 netrc → 注释掉下面这行或保持默认（脚本同目录的 cookies.txt）
#
#  ~/.netrc 格式（Windows: C:/Users/<你的用户名>/.netrc）：
#    machine urs.earthdata.nasa.gov login <USERNAME> password <PASSWORD>
# =============================================================================
COOKIE_FILE="${SCRIPT_DIR}/cookies.txt"

# =============================================================================
#  以下内容无需修改 — 打印配置摘要并启动 Python 脚本
# =============================================================================

echo "========================================================================"
echo " OPERA RTC-S1 v15  |  $(date '+%Y-%m-%d %H:%M:%S')"
echo "========================================================================"
echo "  [区域]   ROI: ${ROI_MINLON},${ROI_MINLAT} ~ ${ROI_MAXLON},${ROI_MAXLAT}"
echo "  [时间]   ${START_DATE} ~ ${END_DATE}"
echo "  [极化]   ${POLARIZATION}"
echo "  [坐标系] ${OUTPUT_CRS}  (分辨率 ${OUTPUT_RESOLUTION}°)"
echo "  [合成]   ${COMPOSITE_MODE}  |  TV去斑: ${DO_TV} (reg=${TV_REG}, eps=${TV_EPS})"
echo "  [裁切]   ${DO_CLIP}  |  输出目录: ${BASE_DIR}"
echo "  [中间文件] 单日TIF保留:${KEEP_DAILY}  原始合成:${DEBUG_RAW}  保留原始:${KEEP_RAW_COMPOSITE}"
echo "  [并发]   ${WORKERS} 线程  |  重试 ${MAX_RETRIES} 次"
echo "========================================================================"

# 认证状态提示
if [ -f "${COOKIE_FILE}" ]; then
    echo "  [认证]  ✅ Cookie 文件存在：${COOKIE_FILE}"
else
    echo "  [认证]  ℹ️  Cookie 不存在，将通过 ~/.netrc 登录"
    echo "          ~/.netrc 需包含：machine urs.earthdata.nasa.gov login <USER> password <PASS>"
fi
echo "========================================================================"
echo ""

# 检查 Python 解释器
if [ ! -f "${PYTHON}" ] && ! command -v "${PYTHON}" &>/dev/null; then
    echo "❌ 错误：找不到 Python 解释器：${PYTHON}"
    echo "   请修改脚本 [0] 部分的 PYTHON 变量"
    exit 1
fi

# 启动 Python 流水线
"${PYTHON}" "${PY_SCRIPT}" \
    --aoi                 "${AOI_WKT}" \
    --roi-bounds          "${ROI_MINLON}" "${ROI_MINLAT}" "${ROI_MAXLON}" "${ROI_MAXLAT}" \
    --start               "${START_DATE}" \
    --end                 "${END_DATE}" \
    --polarization        "${POLARIZATION}" \
    --base-dir            "${BASE_DIR}" \
    --resolution          "${OUTPUT_RESOLUTION}" \
    --output-crs          "${OUTPUT_CRS}" \
    --composite           "${COMPOSITE_MODE}" \
    --tv-reg              "${TV_REG}" \
    --tv-max              "${TV_MAX}" \
    --tv-min              "${TV_MIN}" \
    --tv-interp           "${TV_INTERP}" \
    --tv-eps              "${TV_EPS}" \
    --do-tv               "${DO_TV}" \
    --do-clip             "${DO_CLIP}" \
    --debug-raw           "${DEBUG_RAW}" \
    --keep-daily          "${KEEP_DAILY}" \
    --keep-raw-composite  "${KEEP_RAW_COMPOSITE}" \
    --workers             "${WORKERS}" \
    --max-retries         "${MAX_RETRIES}" \
    --retry-delay         "${RETRY_DELAY}" \
    --cookie              "${COOKIE_FILE}"

EXIT_CODE=$?
echo ""
echo "========================================================================"
if [ "${EXIT_CODE}" -eq 0 ]; then
    echo " ✅  流水线正常完成  |  $(date '+%Y-%m-%d %H:%M:%S')"
else
    echo " ❌  流水线异常退出（exit code: ${EXIT_CODE}）"
fi
echo "========================================================================"
exit "${EXIT_CODE}"
